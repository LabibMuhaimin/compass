<h2 style="text-align:center;">Hakaluki Haor</h2><br><br>

<h4>Hakaluki Haor is a marsh wetland ecosystem of north-eastern Bangladesh. It is one of Bangladesh's largest and one of Asia's larger marsh wetland resources.<br>Hakaluki Haor is bounded by the Kushiara river as well as a part of the Sonai Bardal river to the north, by the Fenchuganj-Kulaura railway to the west and to the south, and by the Kulaura-Beanibazar road the east. It lies between 24°35’ N to 24°44’ N and 92°00’ E to 92°08’ E.<br>

A total of 558 species of animals and birds have been identified here, including some very rare – already declared as threatened, vulnerable, endangered and critically endangered species. Some 190,000 people live in the surrounding Hakaluki haor area.<br>

Hakaluki Haor was designated an Ecologically Critical Area (ECA). </h4><br>
