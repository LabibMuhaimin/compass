<h4>Le Mont-Saint-Michel is a tidal island and mainland commune in Normandy, France.

The island lies approximately one kilometre (0.6 miles) off the country's north-western coast, at the mouth of the Couesnon River near Avranches and is 7 hectares (17 acres) in area. The mainland part of the commune is 393 hectares (971 acres) in area so that the total surface of the commune is 400 hectares (988 acres).

As of 2017, the island has a population of 30.<br>

The commune's position—on an island just a few hundred metres from land—made it accessible at low tide to the many pilgrims to its abbey, but defensible as an incoming tide stranded, drove off, or drowned would-be assailants. The island remained unconquered during the Hundred Years' War; a small garrison fended off a full attack by the English in 1433.[7] Louis XI recognised the reverse benefits of its natural defence and turned it into a prison. The abbey was used regularly as a prison during the Ancien Régime. </h4><br>
