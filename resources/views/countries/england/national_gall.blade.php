<h2 style="text-align:center;">The National Gallery</h2><br><br>

<h4>The National Gallery is an art museum in Trafalgar Square in the City of Westminster, in Central London. Founded in 1824, it houses a collection of over 2,300 paintings dating from the mid-13th century to 1900.[note 1]

The Gallery is an exempt charity, and a non-departmental public body of the Department for Digital, Culture, Media and Sport.[3] Its collection belongs to the government on behalf of the British public, and entry to the main collection is free of charge. In 2019, it was ranked seventh in the world on the List of most visited art museums.[4]

Unlike comparable museums in continental Europe, the National Gallery was not formed by nationalising an existing royal or princely art collection. It came into being when the British government bought 38 paintings from the heirs of John Julius Angerstein in 1824. After that initial purchase the Gallery was shaped mainly by its early directors, especially Sir Charles Lock Eastlake, and by private donations, which now account for two-thirds of the collection.[5] The collection is smaller than many European national galleries, but encyclopaedic in scope; most major developments in Western painting "from Giotto to CÃ©zanne"[6] are represented with important works. It used to be claimed that this was one of the few national galleries that had all its works on permanent exhibition,[7] but this is no longer the case.

The present building, the third to house the National Gallery, was designed by William Wilkins from 1832 to 1838. Only the facade onto Trafalgar Square remains essentially unchanged from this time, as the building has been expanded piecemeal throughout its history. Wilkins's building was often criticised for the perceived weaknesses of its design and for its lack of space; the latter problem led to the establishment of the Tate Gallery for British art in 1897. </h4><br>
