<h2 style="text-align:center;">Eden Project</h2><br><br>

<h4>The Eden Project (Cornish: Edenva) is a visitor attraction in Cornwall, England, UK. Inside the two biomes are plants that are collected from many diverse climates and environments. The project is located in a reclaimed china clay pit, located 2 km (1.2 mi) from the town of St Blazey and 5 km (3 mi) from the larger town of St Austell.[2]

The complex is dominated by two huge enclosures consisting of adjoining domes that house thousands of plant species,[3] and each enclosure emulates a natural biome. The biomes consist of hundreds of hexagonal and pentagonal, Ethylene tetrafluoroethylene (ETFE) inflated cells supported by Geodesic tubular steel domes. The largest of the two biomes simulates a rainforest environment (and is the largest indoor rainforest in the world)[4] and the second, a Mediterranean environment. The attraction also has an outside botanical garden which is home to many plants and wildlife native to Cornwall and the UK in general; it also has many plants that provide an important and interesting backstory, for example, those with a prehistoric heritage.

There are plans to build an Eden Project North in the seaside town of Morecambe, Lancashire, with a focus on the marine environment. </h4><br>
