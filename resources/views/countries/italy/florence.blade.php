<h2 style="text-align:center;">Florence Duomo Santa Maria del Fiore</h2><br><br>

<h4>Florence Cathedral, formally the Cattedrale di Santa Maria del Fiore (Italian pronunciation: [katteˈdraːle di ˈsanta maˈriːa del ˈfjoːre]; in English "Cathedral of Saint Mary of the Flower"), is the cathedral of Florence, Italy (Italian: Duomo di Firenze). It was begun in 1296 in the Gothic style to a design of Arnolfo di Cambio and was structurally completed by 1436, with the dome engineered by Filippo Brunelleschi. The exterior of the basilica is faced with polychrome marble panels in various shades of green and pink, bordered by white, and has an elaborate 19th-century Gothic Revival façade by Emilio De Fabris.

The cathedral complex, in Piazza del Duomo, includes the Baptistery and Giotto's Campanile. These three buildings are part of the UNESCO World Heritage Site covering the historic centre of Florence and are a major tourist attraction of Tuscany. The basilica is one of Italy's largest churches, and until the development of new structural materials in the modern era, the dome was the largest in the world. It remains the largest brick dome ever constructed. </h4><br>
